package ReviewClass02;

public class SimpleWhileLoops {

	public static void main(String[] args) {

		int var = 0;
		while(var<11) {
			System.out.println("Value of var "+var);
			var++;
		}
		System.out.println();
		System.out.println();
		int var1 = 10;
		while(var1>=0) {
			System.out.println("Value of var "+var1);
			var1--;
		}
		
	}

}
